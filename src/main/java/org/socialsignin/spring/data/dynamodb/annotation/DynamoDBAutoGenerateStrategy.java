package org.socialsignin.spring.data.dynamodb.annotation;

/**
 * Strategy for auto-generating timestamp values in SDK_V1_COMPATIBLE mode.
 *
 * <p>Used with {@link DynamoDBAutoGeneratedTimestamp} to control when timestamps
 * are automatically generated.
 *
 * @author Michael Lavelle
 * @author Sebastian Just
 * @since 7.0.0
 * @see DynamoDBAutoGeneratedTimestamp
 */
public enum DynamoDBAutoGenerateStrategy {
    /**
     * Generate timestamp on entity creation only.
     * The value will not be updated on subsequent saves.
     */
    CREATE,

    /**
     * Generate timestamp on both entity creation and updates.
     * The value will be updated every time the entity is saved.
     */
    ALWAYS
}
